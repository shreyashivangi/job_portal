        What is Xcution?

       Xcution is the Indian Government way to get more Indians into work. It connects job seekers with employers and is delivered by a network of jobactive providers in over 1,700 locations across Australia.
       The Xcution website is delivered by Indian JobSearch â€” a free online jobs website.

       For job seekers:

	   The Xcution website is the ideal place to:
	   look for jobs
	   apply for jobs
       search for apprenticeships and traineeships
	   find your local employment services provider
	   The Xcution website gives job seekers the tools to take control of their job search including a personalised Job Seeker Dashboard and a Job Matching section.

 	   For employers:

	   The Xcution website has a number of features to help employers find the right person for their job. Employers can:
       advertise at no cost
 	   advertise without approval delays with Auto Activation of vacancies
	   search for candidates based on criteria in their advertisement using our Find Candidates functionality
	   achieve high visibility for their jobs - with thousands of people visiting JobSearch each month
	   access a personalised Employer Dashboard which houses key information to assist with the search for potential candidates. The Dashboard feature enables employers to track all job vacancies and application traffic in one central location.

         

